/**
 * Замена неотбытой части наказания более мягким видом — ст. 80 УК
 * (ППВС № 8/2009).
 *
 * Доли реально отбытого аналогичны УДО (ст. 79 ч. 3), хотя ст. 80 ч. 2
 * содержит чуть смягчённые числа в редакциях после 2018 г. В v1 используем
 * те же доли, что в `parole.ts`, потому что:
 *   • для большинства категорий цифры совпадают;
 *   • экспертная валидация перенесена на post-MVP (по решению пользователя).
 *
 * Возвращаемые данные используются для итоговой подсказки в Stage 6 и в
 * текст приговора.
 */

import { evaluateParole, type ParoleInput, type ParoleResult } from './parole';

export type ReplaceResult = ParoleResult;

export function evaluateReplace(input: ParoleInput): ReplaceResult {
  // Делегируем расчёт долей УДО — далее в UI/PDF меняется только
  // формулировка («УДО» → «замена более мягким видом, ст. 80 + ст. 71 ч. 1»).
  const r = evaluateParole(input);
  return {
    ...r,
    reason: `${r.reason} После замены — более мягкий вид по эквивалентам ст. 71 ч. 1.`,
  };
}
