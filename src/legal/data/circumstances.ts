/**
 * Перечни смягчающих (УК ст. 61 ч. 1) и отягчающих (УК ст. 63 ч. 1)
 * обстоятельств. Используется на Этапе 3 (UI чек-листы) и на Этапе 4
 * (Master Engine — триггеры коэффициентов).
 *
 * Идентификаторы соответствуют буквам пунктов в УК и используются как ключи
 * `EpisodeFlags.mitigatingIds` / `aggravatingIds`.
 */

export interface Circumstance {
  /** Буква пункта в УК. Для отягчающих с префиксом 'ag_' для уникальности. */
  id: string;
  /** Человекочитаемое описание. */
  label: string;
  /** Полная ссылка на норму. */
  norm: string;
  /**
   * `triggersArt62_1: true` — пункты «и» и «к» ст. 61 ч. 1, наличие любого из
   * которых при отсутствии отягчающих включает правило ст. 62 ч. 1
   * (max ≤ 2/3).
   */
  triggersArt62_1?: boolean;
  /** Краткая подсказка (показывается в UI на ховер). */
  hint?: string;
}

