const MIT_BY_ID = new Map(MITIGATING.map((m) => [m.id, m]));
const AGG_BY_ID = new Map(AGGRAVATING.map((a) => [a.id, a]));

export const getMitigating = (id: string): Circumstance | undefined => MIT_BY_ID.get(id);
export const getAggravating = (id: string): Circumstance | undefined => AGG_BY_ID.get(id);

/** Есть ли в наборе хотя бы один смягчающий из «и»/«к» (триггер ст. 62 ч. 1). */
export function hasArt62_1Trigger(mitigatingIds: string[]): boolean {
  return mitigatingIds.some((id) => MIT_BY_ID.get(id)?.triggersArt62_1);
}
